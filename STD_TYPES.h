#ifndef STD_TYPES_H_
#define STD_TYPES_H_

/*un signed*/
typedef unsigned char       u8; 		// 0 -> 256
typedef unsigned short int u16;
typedef unsigned long  int u32;

/*signed*/
typedef signed char         s8;
typedef signed short int   s16;
typedef signed long  int   s32;

/*float*/
typedef float              f32;
typedef double             f64;


#endif /* STD_TYPES_H_ */
